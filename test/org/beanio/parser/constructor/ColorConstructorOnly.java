package org.beanio.parser.constructor;

public class ColorConstructorOnly {

    private String name;
    private int r;
    private int g;
    private int b;

    public ColorConstructorOnly(String name, int r, int g, int b) {
        this.name = name;
        this.r = r;
        this.g = g;
        this.b = b;
    }

}
